package hust.soict.hedspi.lab01.Bai5;
public class jmath {
    public double sum(double a, double b) {
        return (double)(a+b);
    }

    public double difference(double a, double b) {
        return (double)(a-b);
    }

    public double product(double a, double b) {
        return (double)(a*b);
    }

    public double quotient(double a, double b)  {
        return (double)(a/b);
    }
}
